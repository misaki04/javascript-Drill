// 問題10:
// ある年が与えられます。年がうるう年かどうかを判定し、結果を表示するプログラムを作成してください。

// うるう年の条件:
// 4で割り切れる年はうるう年である。
// ただし、100で割り切れる年はうるう年ではない。
// ただし、400で割り切れる年はうるう年である。
// うるう年の場合: "うるう年です。"
// うるう年でない場合: "うるう年ではありません。"

const year = 2024;

if ((year % 4 === 0 && year % 100 !== 0) || year % 400 === 0) {
  console.log("うるう年です。");
} else {
  console.log("うるう年ではありません。");
}
